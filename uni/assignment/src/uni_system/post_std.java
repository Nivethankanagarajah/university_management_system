            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/form","root","");
            String sql="Select * From post_stu_detail Where Index_No="+view_t_index.getText()+"";
            PreparedStatement pst=con.prepareStatement(sql);

