            String col_name2[]={"Index No","Name","Contact No","Email","Degree","Gender","Faculty","Address","Date Of Birth","Qualification","Institute","Year"};
            tablem_2.setColumnIdentifiers(col_name2);
            ResultSetMetaData rsmd1=result_s.getMetaData();
